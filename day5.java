public class day5 {
    public static void main(String[] args) {
        //1.byte(8-bit)
        byte jumlahkursi = 40;

        //2.short (16-bit)
        short hargabuku = 25000;

        //3.int(32-bit)
        int jumlahmahasiswa = 1350000;

        //4.long(64-bit)
        long totalPengunjung = 5000000000L;

        System.out.println(jumlahkursi);
        System.out.println(hargabuku);
        System.out.println(jumlahmahasiswa);
        System.out.println(totalPengunjung);

    }
}
